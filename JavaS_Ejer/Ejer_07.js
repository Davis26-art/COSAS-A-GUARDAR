//Salon
// Determinar si el estudiante pertenece al salón A o B según su número
let numeroEstudiante = 12;
let salon = numeroEstudiante <= 15 ? 'Salón A' : 'Salón B';
console.log(salon);
// Si el número del estudiante es menor o igual a 15, está en el Salón A; de lo contrario, en el B.


//Nivel 
// Evaluar el nivel de un curso según la nota promedio
let notaPromedio = 4.3;
let nivel = notaPromedio >= 3.5 ? 'Nivel Avanzado' : 'Nivel Básico';
console.log(nivel);
// Si la nota promedio es 3.5 o más, el estudiante está en nivel avanzado; si no, en básico.


